package data;

public class Data {

    private String password;
    private String username;
    private String effectiveDatePolicy;
    private String expiryDatePolicy;

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getEffectiveDatePolicy() {
        return effectiveDatePolicy;
    }

    public void setEffectiveDatePolicy(String effectiveDatePolicy) {
        this.effectiveDatePolicy = effectiveDatePolicy;
    }

    public String getExpiryDatePolicy() {
        return expiryDatePolicy;
    }

    public void setExpiryDatePolicy(String expiryDatePolicy) {
        this.expiryDatePolicy = expiryDatePolicy;
    }
}
